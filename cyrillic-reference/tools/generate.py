#!/usr/bin/env python3
"""Generate the Cyrillic Reference pan-Cyrillic character table.

Reads the aggregated character set from the Paratype Cyrillic language
knowledge base (a sibling `cyrillic-languages/` repository) and writes a
master table of every single Cyrillic character used across the 77
languages in scope, with its Unicode codepoint and descriptive name.

Downstream stages add:
  * decomposition of composed characters ("… WITH MACRON", "… WITH
    DIAERESIS AND BREVE", …) into base + combining-mark sequences,
  * Private Use Area (PUA) → standard-sequence mapping with a legacy
    PUA column for backwards compatibility,
  * SVG illustrations of each letter via the glyphplotter tool.

Usage:
    python3 generate.py
    python3 generate.py --data ../cyrillic-languages --out .

Python 3.8+ stdlib only.
"""

import argparse
import json
import re
import sys
import unicodedata
from pathlib import Path


PUA_RANGE = (0xE000, 0xF8FF)
PUA_TOKEN = re.compile(r"^!([0-9A-Fa-f]{4,5})$")

# Sentinel for the Cyrillic-specific breve — resolved per case in decompose().
# The generic combining breve U+0306 is NOT used here: Paratype Expert fonts
# carry a distinct Cyrillic breve at U+F6D1 (uppercase) and U+F6D4 (lowercase)
# that is the shape typographers expect on top of a Cyrillic base.
_CYRILLIC_BREVE = object()
CYRILLIC_BREVE_UC = 0xF6D1
CYRILLIC_BREVE_LC = 0xF6D4

ACCENT_COMBINING_MARK = {
    "MACRON":       0x0304,
    "DIAERESIS":    0x0308,
    "BREVE":        _CYRILLIC_BREVE,
    "ACUTE":        0x0301,
    "CIRCUMFLEX":   0x0302,
    "GRAVE":        0x0300,
    "CARON":        0x030C,
    "DOUBLE ACUTE": 0x030B,
    "CEDILLA":      0x0327,
    "DOT ABOVE":    0x0307,
}

_WITH_RE = re.compile(r"\s+WITH\s+", re.IGNORECASE)
_AND_RE = re.compile(r"\s+AND\s+", re.IGNORECASE)

